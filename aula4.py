#Tipos int e float
#int: números inteiros
#float: números decimais

#Exemplo de operações com int e float
print(11) #int
print(-11) #int
print(0)    #int

print(11.0) #float
print(-11.0) #float
print(0.0) #float

#A função type mostra o tipo que o valor está armazenado na variável
print(type(11)) #int
print(type(-11)) #int
print(type(0)) #int

print(type(11.0)) #float
print(type(-11.0)) #float
print(type(0.0)) #float

print(11 + 11) #int